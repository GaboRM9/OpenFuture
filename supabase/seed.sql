## Steelman

The strongest case that AI music does not dominate in 3 years: music consumption is fundamentally parasocial. People do not just stream songs — they follow artists, attend concerts, watch interviews, and build identity around fandom. AI has no face, no backstory, no beef, no heartbreak. The entire promotional infrastructure of the music industry — press, social media, touring, brand deals — requires a human anchor. The Hot 100 is not a pure meritocracy of sonic quality; it is a reflection of cultural attention. Until AI can manufacture cultural presence — not just audio — it will remain a production tool, not a chart contender.

## Pre-Mortem

The most likely single cause of this forecast missing: Billboard changes its eligibility definition in response to label lobbying in a direction that *further restricts* AI participation rather than liberalizes it. If the Recording Academy and major DSPs align on a stricter "meaningful human authorship" standard with legal backing, the structural ceiling becomes a floor, and the 28% base scenario probability collapses to near zero within 18 months.

## Conclusion

Most likely outcome: AI becomes invisible infrastructure in music production — credited to humans, powering most of what you hear, but never "dominating" the Hot 100 in a way that is legible on the chart surface. The hybrid scenario (52%) is the one to bet on through 2029.

Most important variable to watch: Billboard's next eligibility rule revision, scheduled for review in Q3 2026. The direction of that ruling sets the ceiling for the following 3 years.

Sensitivity statement: If Billboard liberalizes eligibility from "human contributor required" to "human curator required," probability of 5+ simultaneous Hot 100 AI positions shifts from 28% to approximately 55%.

Invalidation condition: A cross-platform agreement explicitly barring AI-disclosed tracks from all major chart eligibility before end of 2026 would collapse the optimistic scenario below 10% and force a full recalibration around the hybrid/pessimistic split.
