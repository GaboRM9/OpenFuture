- **Governments mandate "human in the loop" requirements for software in critical infrastructure (28%)** — creates a protected employment floor for senior engineers in finance, healthcare, and government sectors

## Steelman

The strongest case that software engineers are not largely replaced in 5 years: software is not a solved problem. The more AI lowers the cost of writing code, the more software gets written, the more complex systems become, and the more there is to maintain, secure, and reason about. Every layer of abstraction in software history — from assembly to C to managed languages to cloud infrastructure — was supposed to reduce programmer demand. Each one expanded it. AI is likely the next layer, not the last one. The engineers who understand what AI-generated code is actually doing will be more valuable, not less.

## Pre-Mortem

The most likely single cause of this forecast missing on the optimistic side: AI agents crack the context problem — maintaining coherent understanding of large, legacy codebases with ambiguous requirements — before 2028. Current AI coding failures cluster heavily around this limitation. If a model demonstrates reliable full-codebase reasoning on a 500k+ line enterprise system, the restructuring timeline compresses significantly and the 12% displacement scenario probability roughly doubles.

## Conclusion

Most likely outcome: software engineering restructures rather than contracts. Total employment stays within 20% of 2026 levels by 2031, but the shape of the profession changes — smaller teams, frozen junior pipelines, higher individual leverage, and growing demand for engineers who can direct and audit AI systems rather than write code from scratch.

Most important variable to watch: junior hiring data in Q3 2026. If major tech companies confirm junior freeze policies in earnings calls, the restructuring scenario is confirmed and the timeline for senior role changes moves forward.

Sensitivity statement: If AI agents demonstrate reliable full-codebase reasoning on enterprise-scale systems before 2028 (currently the primary capability gap), probability of >20% employment decline shifts from 19% to approximately 38%.

Invalidation condition: If US software engineer employment grows more than 10% above 2026 levels by 2029, the displacement thesis is falsified and the demand elasticity (Jevons) scenario becomes dominant.
