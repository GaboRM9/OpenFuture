## Current State Assessment

As of Q1 2026, AI coding tools (GitHub Copilot, Cursor, Claude Code, and similar) are used by an estimated 62% of professional software engineers. Layoffs in the technology sector accelerated through 2025, with major firms citing AI productivity gains as a contributing factor alongside macro conditions — though isolating AI's causal contribution remains contested. The Bureau of Labor Statistics projects software developer employment growing 17% through 2030 in its 2024 outlook, while a competing McKinsey analysis estimates 45% of current software engineering tasks are automatable with existing AI as of 2025. The gap between "tasks automatable" and "jobs eliminated" is the central uncertainty in this forecast.

## Base Rate Analysis

Base rate: 8% — historical automation waves (manufacturing, data entry, basic legal research) eliminated specific task clusters but rarely entire professions within a 5-year window. The accounting and radiology precedents suggest AI handles sub-tasks while human roles restructure around higher-order judgment.

[+18%] Current AI coding capabilities already handle full feature implementation end-to-end for well-specified problems — qualitatively different from prior automation (epistemic)
[+10%] Economic incentive is unusually strong: median US software engineer salary is $130k; replacing even 30% of output with AI has massive ROI (aleatory)
[-15%] "Most jobs" requires >50% employment reduction — no automation wave has achieved this in a 5-year window in a white-collar profession (aleatory)
[-8%] Software complexity and requirements ambiguity continue to expand the scope of what engineers are asked to do — Jevons paradox applies (epistemic)
[-6%] Organizational inertia, security reviews, and compliance requirements slow AI adoption in enterprise contexts (epistemic)
────
Final estimate: ~12% probability that software engineering employment falls by more than 50% within 5 years

Prediction market: 9% (Metaculus, n=1,847) — model estimate slightly higher; divergence explained by this forecast weighting recent capability acceleration more heavily than the Metaculus crowd, which includes many software engineers with professional skepticism bias.

## Key Drivers & Trends

1. **AI coding capability trajectory** — the gap between "AI can write code" and "AI can own a codebase" is narrowing faster than most 2023 predictions anticipated (highest impact)
2. **Task vs. job distinction** — automating 70% of tasks does not eliminate the role; it restructures it toward architecture, requirements, and review — which are harder to automate
3. **Demand elasticity** — cheaper software production historically expanded total software demand; the same dynamic may absorb productivity gains into more software, not fewer engineers
4. **Enterprise adoption lag** — Fortune 500 procurement, security review, and change management cycles mean AI capability in 2026 becomes enterprise standard in 2028–2029
5. **Junior pipeline collapse** — the most observable near-term effect: junior hiring freezes as AI handles entry-level tasks, shrinking the pipeline of future senior engineers

## Scenario Analysis

**Optimistic for AI — Structural Displacement (12%)**
AI agents become reliable enough to own full development cycles for well-scoped projects by 2027 → companies restructure engineering orgs from 100-person teams to 10-person AI-oversight teams → software engineer employment falls 40–60% by 2031. Productivity gains do not generate proportional new demand because software is already abundant.
Capability threshold crossed → economic incentive drives rapid restructuring → employment contracts faster than new roles absorb displaced workers.

**Base Case — Restructuring Without Replacement (68%)**
AI handles 50–70% of coding tasks by 2028, but total employment remains within 20% of 2026 levels. Engineers shift toward architecture, product reasoning, AI supervision, and security. Junior hiring freezes; senior demand holds. The profession looks different — smaller teams, higher individual leverage — but has not been "replaced."

**Pessimistic for AI — Plateau and Integration (20%)**
AI coding tools plateau in capability around current levels for 2–3 years due to context window limits, hallucination in large codebases, and enterprise trust deficits. Adoption slows. Engineering employment continues growing per BLS projections. AI becomes a productivity multiplier comparable to IDEs or Stack Overflow — important but not transformative at the job level.

